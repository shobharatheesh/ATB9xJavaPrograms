package Learning.ex_03102024;

public class EvenOrOdd {
    public static void main(String[] args) {

        int number = 12;

        //check if the number is even or odd

        if(number % 2 == 0)
        {
            System.out.println(number +" is even number. ");

        }

        else
        {
            System.out.println(number+ " is odd number. ");
        }
    }

}
